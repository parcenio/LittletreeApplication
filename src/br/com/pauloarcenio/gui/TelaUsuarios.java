    private void jbCadastrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarUsuarioActionPerformed
        String login = jtLogin.getText().trim().toLowerCase();
        String senha = jtSenha.getText().trim().toLowerCase();
        boolean retornoSenha = TelaInicio.senhaADM();
        if (retornoSenha) {
            Usuario user = new Usuario(login, senha);
            UsuarioDAO.inserir(user);
            atualizaUsuarios();
        } else {
            JOptionPane.showMessageDialog(null, "Senha Inválida!");
        }

        jtLogin.setText("");
        jtSenha.setText("");
    }//GEN-LAST:event_jbCadastrarUsuarioActionPerformed

    private void jbDeletarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarUsuarioActionPerformed
        int selecionado = jtbUsuarios.getSelectedRow();
        if (jtbUsuarios.isRowSelected(selecionado)) {
            String idString = (String) jtbUsuarios.getValueAt(selecionado, 0);
            byte id = Byte.parseByte(idString);
            boolean retornoSenha = TelaInicio.senhaADM();
            if (retornoSenha) {
                UsuarioDAO.remover(UsuarioDAO.selecionaPeloID(id));
                atualizaUsuarios();
            } else {
                JOptionPane.showMessageDialog(null, "Senha inválida!");
            }
        } else {
            Base.mensagem("Selecione um usuário para apagar.");
        }
        jtLogin.setText("");
        jtSenha.setText("");
    }//GEN-LAST:event_jbDeletarUsuarioActionPerformed

    private void jbAlterarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarUsuarioActionPerformed
        int selecionado = jtbUsuarios.getSelectedRow();
        if (jtbUsuarios.isRowSelected(selecionado)) {
            String idString = (String) jtbUsuarios.getValueAt(selecionado, 0);
            byte id = Byte.parseByte(idString);
            Usuario user = (UsuarioDAO.selecionaPeloID(id));
            boolean retornoSenha = TelaInicio.senhaADM();
            if (retornoSenha) {
                String login = jtLogin.getText();
                String senha = jtSenha.getText();
                if (!login.isEmpty() && !senha.isEmpty()) {
                    user.setLogin(login);
                    user.setSenha(senha);
                    UsuarioDAO.alterar(user);
                    atualizaUsuarios();
                } else {
                    Base.mensagem("Insira os dados para alterar!");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Senha inválida!");
            }

        } else {
            Base.mensagem("Selecione um usuário para alterar.");
        }
        jtLogin.setText("");
        jtSenha.setText("");    }//GEN-LAST:event_jbAlterarUsuarioActionPerformed

    private void jtbUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbUsuariosMouseClicked
        int selecionado = jtbUsuarios.getSelectedRow();
        if (jtbUsuarios.isRowSelected(selecionado)) {
            String idString = (String) jtbUsuarios.getValueAt(selecionado, 0);
            byte id = Byte.parseByte(idString);
            Usuario user = UsuarioDAO.selecionaPeloID(id);

            jtLogin.setText(user.getLogin());
            jtSenha.setText(user.getSenha());

        }
    }//GEN-LAST:event_jtbUsuariosMouseClicked

