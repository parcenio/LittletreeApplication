    private void atualizaUsuarios() {
        // TABELA DE USUARIOS
        String[] cposUsuarios = {"ID", "Login", "Senha"};
        List<Usuario> usuarios = UsuarioDAO.selecionarTodos();
        String[][] dadosUsuarios;
        dadosUsuarios = new String[usuarios.size()][3];
        int posicao = 0;
        for (Usuario user : usuarios) {
            String[] umUsuario = new String[3];
            umUsuario[0] = String.valueOf(user.getId());
            umUsuario[1] = user.getLogin();
            umUsuario[2] = String.valueOf(user.getSenha());
            dadosUsuarios[posicao++] = umUsuario;
        }
        DefaultTableModel modeloUser = new DefaultTableModel(
                dadosUsuarios, cposUsuarios);
        jtbUsuarios.setModel(modeloUser);
    }

